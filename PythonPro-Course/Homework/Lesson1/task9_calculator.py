numer1 = float(input(""))
wybór = input("Wybór operacji: ")
numer2 = float(input(""))
if wybór == "+":
    print(f"{numer1 + numer2}")
elif wybór == "-":
    print(f"{numer1 - numer2}")
elif wybór == "*":
    print(f"{numer1 * numer2}")
elif wybór == "/":
    print(f"{numer1 / numer2}")


        
        


    
    
    






